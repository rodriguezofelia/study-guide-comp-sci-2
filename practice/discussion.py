# Graphs
# What is a graph?
# It's a data structure that has nodes and edges. The nodes (or vertices) are connected by edges (or arcs). 
# Nodes are units of data and the edges (arcs (think of link)) are the connections between the relationships of nodes.

# Give an example of something that would be good to model with a graph.

# A food chain that shows every single arrow indicating which animal preys on each other. 
